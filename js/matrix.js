const tiles = [];

export function makeTile(x, y, context, tileSize) {
    tileMatrix.forEach((row, rowIndex) => {
        row.forEach((cell, colIndex) => {
            if (cell === 1) {
                x = colIndex * tileSize;
                y = rowIndex * tileSize;
                tiles.push(new Tile(x, y, context, tileSize));
            }
        });
    });
    return tiles;
}


